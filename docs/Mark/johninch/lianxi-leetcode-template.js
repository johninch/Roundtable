var isValid = function(s) {
	const stack = [];
	const len = s.length;
	if (len % 2) return false;

	for (let i = 0; i < len; i++) {
		let letter = s[i];
		switch (letter) {
			case "(":
			case "{":
			case "[":
				stack.push(letter);
				break;
			case ")":
				if (stack.pop() !== "(") return false;
				break;
			case "}":
				if (stack.pop() !== "{") return false;
				break;
			case "]":
				if (stack.pop() !== "[") return false;
				break;
			default:
				return false;
		}
	}

	return !stack.length;
};

